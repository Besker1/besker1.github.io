/* Loops
 are used to iterate the piece of code using for, while,
 do while or for-in loops. It makes the code compact. It is mostly used in array.

There are four types of loops in JavaScript.

for loop
while loop
do-while loop
for-in loop
*/

// for Loop example
for (i=1; i<=5; i++)  {
    console.log(i);
}

// while loop example
var i=11;  
while (i<=15)  
{  
console.log(i);  
i++;  
}  


// do while loop example
var i=21;  
do{  
console.log(i);  
i++;  
}while (i<=25);  

function drawChessboard(size) {
    // declare a variable
    //to compare
    // make a for loop 
    // make another for loop inside the first
    // make an if statement 
    // add both variables from both loops and compare
    // return board = "\n"
    
    let board = "";
    
    for(let i = 0; i < size; i++){
        for(let k = 0; k < size; k++){
            if((i + k) % 2 === 0){
                board += " ";
            }else {
                board += "#";
                }
        }
        board += "\n";
    }
    
    console.log(board);
    
}

